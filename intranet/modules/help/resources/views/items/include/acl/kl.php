<ol>
    <li>Manage field: Quản lý field. Thêm, sửa, xóa các field. Các field này có thể là field để đánh tag, cũng có thể là field để nhập dữ liệu</li>
    <li>Manage old project: &nbsp;Quản lý project cũ. Thêm project cũ.
        <ol>
            <li>Quyền company: Được quản lý project cũ của công ty</li>
            <li>Quyền team: Được quản lý project cũ của team mình</li>
            <li>Note: Cấp thêm quyền view project tagging để xem được danh sách các project</li>
        </ol>
    </li>
    <li>View project tagging: Xem&nbsp;project để đánh tag. Khi cấp quyền này, cấp thêm quyền&nbsp;<strong>View project detail popup</strong>&nbsp;cho người đó để xem được chi tiết project
        <ol>
            <li>Quyền company: Được xem tất cả project của công ty</li>
            <li>Quyền team: Được xem tất cả project của team</li>
            <li>Quyền cá nhân: Được xem tất cả project của mình (là member, sale, group leader)</li>
        </ol>
    </li>
    <li>View project search:&nbsp;Xem&nbsp;project trong trang search. Khi cấp quyền này, cấp thêm quyền&nbsp;<strong>View project detail popup</strong>&nbsp;cho người đó để xem được chi tiết project
        <ol>
            <li>Quyền company: Search tất cả project của công ty</li>
            <li>Quyền team: Search tất cả project của team</li>
            <li>Quyền cá nhân: Search tất cả project của mình (là member, sale, group leader)</li>
        </ol>
    </li>
    <li>View project detail popup: Xem chi tiết project
        <ol>
            <li>Note: đi kèm với quyền <strong>View project tagging</strong> và <strong>View project search</strong></li>
        </ol>
    </li>
    <li>Submit project tag: Quyền submit project sau khi đánh tag. Nếu một người có quyền approve, thì khi submit project tag, project tag đó tự động được approve.
        <ol>
            <li>Quyền company: Submit tất cả project của công ty</li>
            <li>Quyền team: Submit tất cả project của team</li>
            <li>Quyền cá nhân: Submit tất cả project assign cho mình</li>
            <li>Note: Không cấp quyền cho ,A mà A được assign, thì A vẫn submit được</li>
        </ol>
    </li>
    <li>Approve project tag: Quyền approve&nbsp;project sau khi submit project tag.
        <ol>
            <li>Quyền company: Submit tất cả project của công ty</li>
            <li>Quyền team: Submit tất cả project của team</li>
            <li>Quyền cá nhân: Submit tất cả project assign cho mình</li>
        </ol>
    </li>
</ol>
