<p>Cách truy cập vào trang cá nhân của mình:</p>

<p>Tại trang chủ Intranet, member vào menu Profile, chọn mục Information.</p>

<p>Mọi nhân viên công ty được yêu cầu phải cập nhật các thông tin cá nhân của mình trên trang profile một cách đầy đủ.</p>

<p>Đây là những thông tin cần thiết trong quá trình làm việc của nhân viên tại công ty. Mọi thông tin cá nhân cập nhật đều được bảo mật và phân quyền chặt chẽ, chỉ có cá nhân đó, division manager và các cá nhân có thẩm quyền mới được phép truy cập.</p>

<p>Cụ thể, các thông tin được chia ra thành những mục như dưới đây.</p>
