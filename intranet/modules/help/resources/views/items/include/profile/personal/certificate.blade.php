<p>Danh sách chứng chỉ nhân viên đã có: chứng chỉ về ngoại ngữ, chuyên ngành, kỹ năng mềm, &hellip; Nhân viên được yêu cầu liệt kê ra các chứng chỉ của bản thân có được. Các chứng chỉ có thể là 1 trong các loại sau:</p>
<ul>
    <li>Chứng chỉ ngoại ngữ: Các chứng chỉ liên quan đến trình độ ngoại ngữ của nhân viên, như tiếng Anh: TOEIC, TOEFL, IELTS, &hellip;các chứng chỉ tiếng Nhật như JPLT, NAT-test, BJT&hellip;</li>
    <li>Các chứng chỉ chuyên ngành IT như AWS, Microsoft, CCNA, PMP, PMF ISTQB &hellip;</li>
    <li>Chứng chỉ các kỹ năng mềm như: Kỹ năng thuyết trình, chứng chỉ các khóa kỹ năng lãnh đạo, ...</li>
</ul>

<p>- Nút thêm: thêm mới chứng chỉ.&nbsp;<br />
- Nút xóa: xóa 1 chứng chỉ.</p>
