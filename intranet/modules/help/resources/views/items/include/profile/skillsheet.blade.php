<p><em>Skillsheet là 1 bản CV giản lược của nhân viên, trong đó tập trung vào các thông tin kỹ năng, trình độ, kinh nghiệm làm việc của nhân viên. Skillsheet được dùng khi công ty giới thiệu nhân viên với khách hàng cho mục đích phục vụ dự án, cũng như để leader một phần nắm được trình độ của nhân viên. Trước đây bản skillsheet được quản lý dưới dạng file excel, các leader sẽ là người thu thập skillsheet từ nhân viên, quản lý skillsheet đó và yêu cầu nhân viên cập nhật mỗi khi có thay đổi.</em></p>

<p>Do đó nhu cầu quản lý skillsheet thuận tiện, nhanh gọn là hết sức cần thiết và intranet đã được trang bị thêm 1 tính năng mới là skillsheet online.</p>

<p>Với skillsheet online (nằm trong màn hình profile, tab Skill Sheet), nhân viên sẽ tự cập nhật các kinh nghiệm làm việc, kỹ năng của mình lên hệ thống, sau đó submit để leader review. Bất cứ khi nào nhân viên cũng có thể cập nhật, thêm mới kinh nghiệm của mình sau đó submit tới leader. Ngược lai, bất cứ khi nào cần thiết, leader đều có thể truy cập vào skillsheet của nhân viên và export ra file excel để gửi cho khách hàng.</p>

<p>Skillsheet có 2 phiên bản tiếng Anh và tiếng Nhật. Tùy vào từng ngôn ngữ mà nhân viên nhập vào các thông tin của mình tương ứng. Có hai loại field như bên dưới.</p>

<p><strong>*** <span style="color:#e74c3c;">field lang</span></strong>: Một số trường dữ liệu có giá trị chỉ có hiệu lực ở 1 ngôn ngữ, ở bản tiếng anh nó có giá trị khác với bản tiếng Nhật, khi chuyển sang ngôn ngữ khác, trường dữ liệu đó sẽ bị reset giá trị trước khi thay đổi. Ví dụ: tiếng Anh, trường dữ liệu Address có giá trị &ldquo;Address english 123.&rdquo;, thay đổi thành &ldquo;ADD new 456&rdquo;, không save rồi chuyển sang chọn ngôn ngữ tiếng Nhật thì giá trị bản tiếng Anh ở trường Address vẫn là giá trị cũ &ldquo;Address english 123.&rdquo;</p>

<p><strong>*** <span style="color:#e74c3c;">field normal</span></strong>: Trường dữ liệu có giá trị giống nhau cả ở tiếng Anh lẫn tiếng Nhật.</p>

<p>&nbsp;</p>

<p>Có 3 tab trong skillsheet:</p>

<ol>
	<li>Tab Summary: chứa các thông tin cơ bản của nhân viên.</li>
	<li>Tab Project: chức kinh nghiệm các dự án mà nhân viên đã từng làm từ trước tới nay.</li>
	<li>Tab Skills: bản tóm tắt kỹ năng của nhân viên.</li>
</ol>

<p>Thông tin ở các tab này tương ứng với các thông tin sẽ được export ra skillsheet để gửi cho khách hàng.</p>
