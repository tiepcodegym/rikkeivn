<p dir="ltr">Sau khi sửa xong 1 hàng, click vào button save để save dữ liệu hàng đó</p>

<p dir="ltr">Giải thích các trường dữ liệu:</p>

<p dir="ltr">- Previous Projects (業務内容): tên và miêu tả của dự án (field lang)<br />
- OS, Programming language, Development enviroment: hệ điều hành, ngôn ngữ lập trình, framework, hệ cơ sở dữ liệu, IDE mà dự án dùng. - Bất cứ thuật ngữ chuyên môn nào có liên quan đến dự án đã làm đều list vào. Cách nhập: gõ 2 ký tự và chọn keyword từ danh sách mặc định, &nbsp;hoặc gõ 1 từ bất kỳ nếu chưa có trong danh sách.<br />
- Response for (担当フェーズ) : vai trò đảm nhận trong dự án, có thể chọn từ danh sách hoặc gõ 1 từ bất kỳ (field lang).<br />
- Start / End: thời gian bắt đầu kết thúc dự án (field normal).<br />
- Period: tự động tính khi nhập Start / End<br />
+ Mặc định khi mới vào skill sheet, sẽ có 5 dòng trống để nhập dự án của mình<br />
+ Click button &ldquo;+&rdquo; để thêm dự án, button &ldquo;xóa&rdquo; (thùng rác) để xóa dự án, button &ldquo;edit&rdquo; (bút chì) để edit dự án, button &ldquo;view&rdquo; (con mắt) để chuyển sang chế độ view. Khi thêm sửa xóa, thì phải Save hoặc Submit thì dữ liệu mới bị ảnh hưởng.<br />
&nbsp;</p>
