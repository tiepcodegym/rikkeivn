<p dir="ltr">Giải thích các trường dữ liệu:</p>
<p>- Katakana name: Tên tiếng Nhật của nhân viên, bị disable ở tiếng Anh<br />
- Date of birth, gender: ngày sinh, giới tính, chỉ bộ phận Hành chính, nhân sự mới có quyền thay đổi (field normal)<br />
- Place of birth (本籍地): Nơi sinh<br />
- Address (住所): Địa chỉ hiện tại, thông thường khi gửi cho khách hàng thì nếu ở Việt Nam lấy địa chỉ Rikkeisoft Hà nội, Đà nẵng, nếu đang ở - Nhật thì lấy địa chỉ văn phòng Rikkei Japan.<br />
- Alma Mater (出身校): Tên trường đại học (trung tâm) đã tốt nghiệp và năm tốt nghiệp.<br />
- Personal Statement (自己紹介): Vài dòng tự giới thiệu bản thân, nên list ra những thành tích, chứng chỉ nổi trội mà mình có.<br />
- Japanese level (日本語レベル): Trình độ năng lực tiếng Nhật<br />
- English level (英語レベル): Trình độ năng lực tiếng Anh.<br />
- Compatible Field (対応可能分野): Lĩnh vực công việc nhân viên đang làm, như phát triển hệ thống web, mobile, các hệ thống nghiệp vụ, &hellip;<br />
- Work experience（Years）(業務経験): Số năm kinh nghiệm làm việc của nhân viên.<br />
- Work Experience Oversea (日本常駐経験): Mô tả ngắn gọn nếu làm việc tại nước ngoài, làm ở đâu, bao nhiêu lâu, làm gì?</p>
